# 정수를 입력으로 받아 입력 받은 수를 거꾸로 출력하는 문제이다.
# 입력
# 입력의 첫 수는 수의 개수 n 이다. ( 1 <= n <= 1000 )
# 다음 줄에는 수들이 입력으로 주어진다. 각 수의 범위는 -10000 < n < 10000 이다.
#
# 출력
# 한 줄에 입력받은 수를 거꾸로 출력한다.
# 입출력 예
# 입력
#
# 4
# -9 1 2 3
#
# 출력
#
# 3 2 1 -9
# 출처:
# 채점 데이터:
# [질/답] [제출 현황] [푼 후(3)]
# [ 채 점 ] [홈으로]  [뒤 로]

input_number = int(input())
list_a = input().split()
list_b = []
i = len(list_a) -1
while i >= 0:
    list_b.append(list_a[i])
    i -= 1
print(' '.join(list_b))


